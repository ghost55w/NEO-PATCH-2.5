function determinePrice(rank) {
  const rankPrices = {
    "SS": 3000000,
    "S": 1000000,
    "A": 500000,
    "B": 100000,
  };
  return rankPrices[rank] || 0;
}

// --------------------
// üîµ CAT√âGORIE SELON OVR
// --------------------
function determineCategory(ovr) {
  if (ovr >= 100) return "world_class";
  if (ovr >= 90) return "next_gen";
  if (ovr >= 80) return "rare";
  return "normal";
}

