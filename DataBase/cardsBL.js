// --------------------
// üîµ GROUPER PAR PLACEMENT (SANS goal)
// --------------------
function groupCardsByPlacement(cardsArray) {
  const grouped = {};
  for (const card of cardsArray) {
    const place = card.placement;
    if (!grouped[place]) grouped[place] = [];

    grouped[place].push({
      name: card.name,
      country: card.country,
      rank: card.rank,
      ovr: card.ovr,
      category: card.category,
      image: card.image,
      price: card.price,
      taille: card.taille,
      pieds: card.pieds
    });
  }
  return grouped;
}

